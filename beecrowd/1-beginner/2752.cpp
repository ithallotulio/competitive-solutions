#include <bits/stdc++.h>

using namespace std;

int main() {
    string phrase = "AMO FAZER EXERCICIO NO URI";
    printf("<%s>\n", phrase.c_str());
    printf("<%30s>\n", phrase.c_str());
    printf("<%.20s>\n", phrase.c_str());
    printf("<%-20s>\n", phrase.c_str());
    printf("<%-30s>\n", phrase.c_str());
    printf("<%.30s>\n", phrase.c_str());
    printf("<%30.20s>\n", phrase.c_str());
    printf("<%-30.20s>\n", phrase.c_str());
    return 0;
}
